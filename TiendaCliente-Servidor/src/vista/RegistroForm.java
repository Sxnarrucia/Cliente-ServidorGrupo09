    private void JBRegistrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBRegistrarUsuarioActionPerformed
        if (!JTFEmail.getText().trim().equals("")
                && !JTFPassword.getText().trim().equals("")
                && !JTFUsuario.getText().trim().equals("")) {
            Usuario miUsuario = new Usuario();
            miUsuario.setNombreUsuario(JTFUsuario.getText().trim());
            miUsuario.setPassword(encriptador.codificar(JTFPassword.getText().trim()));
            miUsuario.setEmail(JTFEmail.getText().trim());
            if (conexionBD.setConexion()) {
                if (controlador.insertarUsuario(miUsuario)) {
                    JOptionPane.showMessageDialog(null, "Usuario agregado");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Error al conectar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe rellenar todas las opciones");
        }
    }//GEN-LAST:event_JBRegistrarUsuarioActionPerformed

